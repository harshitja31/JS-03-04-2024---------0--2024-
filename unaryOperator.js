//post increment
let year = 2023;
console.log(year);//2023
let newYear = year++;
console.log(newYear);//2023
console.log(year);//2024

//post decrement
let age = 19;
console.log(age);//19
let newAge = age--;
console.log(newAge);//19
console.log(age);//18

//pre increment
let x = 10;
console.log(x);//10
let y = ++x;
console.log(y);//11
console.log(x);//11

//pre decrement
let a = 20;
console.log(a);//20
let b = --a;
console.log(b);//19
console.log(a);//19

